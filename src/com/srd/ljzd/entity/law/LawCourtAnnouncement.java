package com.srd.ljzd.entity.law;

import java.io.Serializable;
import java.util.Date;

/** 
 * @ClassName: LawCourtAnnouncement
 * @Description: 法院公告
 * @author shiyong
 * @date 2016年5月5日 下午2:07:40
 *  
 */
public class LawCourtAnnouncement implements Serializable{
	private static final long serialVersionUID = -1329062090869401519L;
	private String party;//当事人
	private String announcementContent;//公告内容
	private String announcementType;//公告类型
	private String courtName;//法院名称
	private String courtID;//法院ID
	private String publishDate;//公告时间
	
	public String getParty() {
		return party;
	}
	public void setParty(String party) {
		this.party = party;
	}
	public String getAnnouncementContent() {
		return announcementContent;
	}
	public void setAnnouncementContent(String announcementContent) {
		this.announcementContent = announcementContent;
	}
	public String getAnnouncementType() {
		return announcementType;
	}
	public void setAnnouncementType(String announcementType) {
		this.announcementType = announcementType;
	}
	public String getCourtName() {
		return courtName;
	}
	public void setCourtName(String courtName) {
		this.courtName = courtName;
	}
	public String getCourtID() {
		return courtID;
	}
	public void setCourtID(String courtID) {
		this.courtID = courtID;
	}
	public String getPublishDate() {
		return publishDate;
	}
	public void setPublishDate(String publishDate) {
		this.publishDate = publishDate;
	}

}
