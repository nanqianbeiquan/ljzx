package com.srd.ljzd.entity.common;

/** 
 * @ClassName: ResultBean
 * @Description: 结果类
 * @author shiyong
 * @date 2016年4月20日 上午9:07:15
 *  
 */
public class ResultBean {
	private String resultCode;//0：表示成功； 其它状态表示失败；
	private String resultMsg = "";//结果说明
	private Object resultData;//结果数据
	
	/**
	* <p>Title: </p> 
	* <p>Description: </p>  
	*/
	public ResultBean() {
		super();
	}

	/**
	* <p>Title: </p> 
	* <p>Description: </p> 
	* @param resultCode
	* @param resultMsg
	* @param resultData 
	*/
	public ResultBean(String resultCode, String resultMsg, Object resultData) {
		super();
		this.resultCode = resultCode;
		this.resultMsg = resultMsg;
		this.resultData = resultData;
	}

	/**
	 * @return the resultCode
	 */
	public String getResultCode() {
		return resultCode;
	}

	/**
	 * @param resultCode the resultCode to set
	 */
	public void setResultCode(String resultCode) {
		this.resultCode = resultCode;
	}

	/**
	 * @return the resultMsg
	 */
	public String getResultMsg() {
		return resultMsg;
	}

	/**
	 * @param resultMsg the resultMsg to set
	 */
	public void setResultMsg(String resultMsg) {
		this.resultMsg = resultMsg;
	}

	/**
	 * @return the resultData
	 */
	public Object getResultData() {
		return resultData;
	}

	/**
	 * @param resultData the resultData to set
	 */
	public void setResultData(Object resultData) {
		this.resultData = resultData;
	}

	
	
}
